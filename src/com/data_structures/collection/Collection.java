package com.data_structures.collection;

public class Collection {
    /**
     * Collection 接口
     * Collection 接口存储一组不唯一  无序的对象
     * List -> Collection
     * Set -> Collection
     */

    /**
     * List 接口
     * List 接口存储一组不唯一 有序的对象
     * 允许有相同的数据
     */

    /**
     * Set
     * Set 接口存储一组唯一  无序的对象
     * 不保存重复的数据
     */

    /**
     * SortedSet -> Set
     * 保存有序集合
     */

    /**
     * Map 接口存储一组键值对象 提供key 到 value 的映射
     */

    /**
     * Map.Entry
     * 描述一个Map中的一个元素(键/值) Map的一个内部类
     */

    /**
     * SortMap —> Map
     * 继承于Map 使Key保持在升序排列
     */

    /**
     * Enumeration 枚举
     * 枚举对象集合中的元素  这个传统接口被迭代器取代
     */
}
